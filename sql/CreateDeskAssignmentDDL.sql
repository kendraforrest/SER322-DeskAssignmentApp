CREATE SCHEMA `SER322_FP`;
 
CREATE TABLE `SER322_FP`.`Team` (
  `TeamID` INT NOT NULL,
  `Size` INT NOT NULL,
  `CostCenter` INT NOT NULL,
  `TeamName` VARCHAR(45) NOT NULL,
  `TeamParent` VARCHAR(45) NOT NULL,
