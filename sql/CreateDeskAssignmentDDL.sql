  `DeskID` VARCHAR(45) NOT NULL,
  `Building` VARCHAR(45) NOT NULL,
  `Floor` INT NOT NULL,
  `Section` INT NOT NULL,
  `Aisle` VARCHAR(45) NOT NULL,
  `Desk` INT NOT NULL,
